    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        
        try {
            
             stmt = con.createStatement();
             
             int Sid = Integer.parseInt(id.getText());
             String  sName = name.getText();
             String sEmail = email.getText();
             int sAge = Integer.parseInt(age.getText());
             String sGrade = (String) grade.getSelectedItem();
             int sPhone = Integer.parseInt(phone.getText());
             
             String query = "UPDATE student SET fullname = ? , email = ? , age = ? , grade = ? , phone = ? WHERE id = ?";
             
             PreparedStatement preparedstatement =  con.prepareStatement(query);
             
             
             preparedstatement.setString(1, sName);
             preparedstatement.setString(2, sEmail);
             preparedstatement.setInt(3 , sAge);
             preparedstatement.setString(4, sGrade);
             preparedstatement.setInt(5 , sPhone);
             preparedstatement.setInt(6 , Sid);
             
             preparedstatement.executeUpdate();
             
             JOptionPane.showMessageDialog(null, "Updated");
             
             
             
        } catch (Exception e) {
            
            System.out.println(e);
        }
    }//GEN-LAST:event_updateActionPerformed

