    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        
        try {
            
             stmt = con.createStatement();
             
             int Sid = Integer.parseInt(id.getText());
             String  sName = name.getText();
             String sEmail = email.getText();
             int sAge = Integer.parseInt(age.getText());
             String sGrade = (String) grade.getSelectedItem();
             int sPhone = Integer.parseInt(phone.getText());
             
             String query = "INSERT INTO student(id,fullname,email,age,grade,phone) VALUES (?,?,?,?,?,?)";
             
             PreparedStatement preparedstatement =  con.prepareStatement(query);
             preparedstatement.setInt(1 , Sid);
             preparedstatement.setString(2, sName);
             preparedstatement.setString(3, sEmail);
             preparedstatement.setInt(4 , sAge);
             preparedstatement.setString(5, sGrade);
             preparedstatement.setInt(6 , sPhone);
             
             preparedstatement.execute();
             
             JOptionPane.showMessageDialog(null, "added");
             
        } catch (Exception e) {
            
            System.out.println(e);
        }
    }//GEN-LAST:event_submitActionPerformed

    private void searchIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchIDActionPerformed
        // TODO add your handling code here:
        
        try {
            
            stmt = con.createStatement();
            int sID = Integer.parseInt(SID.getText());
            
            String query = "SELECT * FROM student";
            rs = stmt.executeQuery(query);
            
            while(rs.next()){
                
                if(sID == rs.getInt("id")){
                    
                    id.setText(String.format( "%s", rs.getInt("id")));
                    name.setText(rs.getString("fullname"));
                    email.setText(rs.getString("email"));
                    age.setText(String.format("%s", rs.getInt("age")));
                    grade.setSelectedItem(rs.getString("grade"));
                    phone.setText(String.format(0 + "%s", rs.getInt("phone")));
                }
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_searchIDActionPerformed

